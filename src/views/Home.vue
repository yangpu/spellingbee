<script setup>
import { ref, onMounted, onUnmounted, computed } from 'vue';
import { useWordsStore } from '@/stores/words';
import { useCompetitionStore } from '@/stores/competition';

const wordsStore = useWordsStore();
const competitionStore = useCompetitionStore();

const wordCount = computed(() => wordsStore.wordCount);
const stats = computed(() => competitionStore.stats);

// PWA å®‰è£…ç›¸å…³çŠ¶æ€
const deferredPrompt = ref(null);
const canInstall = ref(false);
const isInstalled = ref(false);

// æ£€æµ‹æ˜¯å¦å·²å®‰è£…ä¸º PWA
function checkIfInstalled() {
  // æ£€æµ‹ standalone æ¨¡å¼ï¼ˆå·²å®‰è£…çš„ PWAï¼‰
  if (window.matchMedia('(display-mode: standalone)').matches) {
    isInstalled.value = true;
    return true;
  }
  // iOS Safari standalone æ£€æµ‹
  if (window.navigator.standalone === true) {
    isInstalled.value = true;
    return true;
  }
  return false;
}

// å¤„ç† beforeinstallprompt äº‹ä»¶
function handleBeforeInstallPrompt(e) {
  e.preventDefault();
  deferredPrompt.value = e;
  canInstall.value = true;
}

// å¤„ç† appinstalled äº‹ä»¶
function handleAppInstalled() {
  canInstall.value = false;
  isInstalled.value = true;
  deferredPrompt.value = null;
}

// å®‰è£…åº”ç”¨
async function installApp() {
  if (!deferredPrompt.value) return;
  
  deferredPrompt.value.prompt();
  const { outcome } = await deferredPrompt.value.userChoice;
  
  if (outcome === 'accepted') {
    canInstall.value = false;
    isInstalled.value = true;
  }
  deferredPrompt.value = null;
}

onMounted(async () => {
  await wordsStore.init();
  await competitionStore.loadRecords();
  
  // æ£€æµ‹æ˜¯å¦å·²å®‰è£…
  checkIfInstalled();
  
  // ç›‘å¬å®‰è£…æç¤ºäº‹ä»¶
  window.addEventListener('beforeinstallprompt', handleBeforeInstallPrompt);
  window.addEventListener('appinstalled', handleAppInstalled);
});

onUnmounted(() => {
  window.removeEventListener('beforeinstallprompt', handleBeforeInstallPrompt);
  window.removeEventListener('appinstalled', handleAppInstalled);
});
</script>

